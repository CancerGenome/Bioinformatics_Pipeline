use strict;
use warnings;
use Getopt::Std;
$ARGV[0] || &usage();
our ($opt_h,$opt_p,$opt_s);
getopts("hps");
my %hash;

while(my $line = <DATA>){
	chomp $line;
	my @F = split/\s+/,$line;
	my $ID = $F[1];
	my $chip_ID;
	if(defined $opt_s){
		$chip_ID = $F[1];
	}else{
		$chip_ID = $F[0]."_".$F[1];
	}
	$ID =~ s/CCFcon/CCF.con-/;
	$ID =~ s/CCF_con/CCF.con/;
	$ID =~ s/CCfcon445/CCF.con-445/;
	$ID =~ s/UMAD-/UMAD/;
	$ID =~ s/UMAD/AD-/;
	$ID =~ s/AD0469/AD-469/;
	$ID =~ s/AD0530/AD-530/;
	$ID =~ s/UBC/UBC-/;
	$ID =~ s/CLEV/CLEV-/;
	$ID =~ s/131_a/131/;
	$ID =~ s/131_b/131/;
	my @G = split/\-/, $ID;
	my $clean_ID;
	if($#G == 0){
		$clean_ID =  $G[0];
	}else{
		my @H = split//,$G[1];
		while($#H < 3){
			unshift(@H, 0);
		}
		$clean_ID = $G[0]."-".join("",@H);
	}

	if(defined $opt_p){
		print STDERR "$chip_ID\t$clean_ID\n";
	}
	$hash{$chip_ID} = $clean_ID;

}

while(my $line = <>){
	chomp $line;
	my @F = split/\s+/,$line;
	if(exists $hash{$F[0]}){
		print $line,"\t",$hash{$F[0]},"\n"
	}else{
		print $line,"\t-\n"
	}
}
