use strict;
use warnings;
use Getopt::Std;
$ARGV[0] || &usage();
our ($opt_h,$opt_d, $opt_p,$opt_P,$opt_o,$opt_t);
getopts("hdp:P:o:t:");

if(not defined $opt_p){
	#$opt_p = "I:\\WES2019_MGI_20200120\\WES2019\\Sort_Dup_BQ";
	$opt_p = "I:\\WES2019_MGI_20200120\\Sort_Dup_BQ";
}
if(not defined $opt_P){
	$opt_P = "dup.bq.bam";
}
if(not defined $opt_o){
	$opt_o = $opt_p."\\IGV";
}

my %hash; # record translated name
while(my $line = <DATA>){
	chomp $line;
	my @F = split/\s+/,$line;
#	push( @{$hash{$F[0]}}, $F[1]);
	push( @{$hash{$F[1]}}, $F[0]);
}

my %trio; # record trio samples
if(defined $opt_t){
	open T, "$opt_t";
	while(my $line = <T>){
		chomp $line;
		my @F = split/\s+/,$line;
		$trio{$F[0]} = $line;
	}
}

my $pre_ID = "";
my $pre_Pos = "";
my $proband = "";
my $chr;
my $pos;
while(my $line = <>){
	chomp $line;
	my @F = split/\s+/,$line;
	my $id = $F[0];
	$chr = "chr".$F[1];
	$pos = $F[2];

	if(defined $opt_d){
		# use directly id, no others 
		$proband = $id;
			if($pre_ID ne $id){
				# if this is new id, load new bam file
				&print_for_each_new();
			}
			# print for all 
			&append_for_each_variant();
			$pre_ID = $id;
		next;
	}
	
	if(exists $hash{$id}){
		my @ID = @{$hash{$id}};
		#print "ID:@ID\n";
		foreach my $i (0..$#ID){
			$proband = $ID[$i];
			if($pre_ID ne $id){
				# if this is new id, load new bam file
				&print_for_each_new();
			}
			# print for all 
			&append_for_each_variant();
			$pre_ID = $id;
		}
	}
}

sub print_for_each_new(){
	# design for each new variants, load data
	print "new\n";
	print "load $opt_p\\$proband.$opt_P\n";
	if(defined $trio{$proband}){
		my @F = split/\s+/,$trio{$proband};
		for my $i (0..$#F){
			print "load $opt_p\\$F[$i].$opt_P\n"; # parent 1
		}
	}
	print "snapshotDirectory $opt_o \n";
	print "collapse\n";
}

sub append_for_each_variant(){
	# design for each exising session, only goto specific variant
	print "goto $chr:$pos-$pos\n";
	print "group strand\n";
	print "sort strand\n";
	print "sort base\n";
	print "snapshot $proband\_$chr\_$pos\.png\n";
}

